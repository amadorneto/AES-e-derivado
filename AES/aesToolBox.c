//Etapa de misturar colunas
//É passado como argumento o ponteiro que aponta para o começo da string que representa o bloco de 16 bytes
//Adaptado de https://en.wikipedia.org/wiki/Rijndael_MixColumns
//OBS: não entendemos o que acontece aqui, e como nosso foco é a implementação do algoritmo e não o entendimento de sua matemática, só aceitamos
void MixColumns(unsigned char *block) {
    
    unsigned char r[4];
    unsigned char a[4];
    unsigned char b[4];
    unsigned char i;
    unsigned char c;
    unsigned char h;

    /* O vetor 'r' é a coluna que está sendo misturada
     * O vetor 'a' é uma cópia do vetor 'r'
     * O vetor 'b' é cada elemento de 'a' dobrado em Campos Finitos de Rijndael
     * a[n] ^ b[n] é o elemento n multiplicado por 3 em Campos Finitos de Rijndael
     * 'i' e 'c' são iteradores */ 
     
     
    for(i = 0; i < 4; i++){
        
        r[0] = block[i];
        r[1] = block[4+i];
        r[2] = block[8+i];
        r[3] = block[12+i];
        
        for (c = 0; c < 4; c++) {
            a[c] = r[c];
            /* h é 0xff se o maior bit de r[c] é 1, 0 caso contrário */
            h = (unsigned char)((signed char)r[c] >> 7); /* shift aritmético à direita */
            b[c] = r[c] << 1; 
            b[c] ^= 0x1B & h; /* Campos Finitos de Rijndael */
    
        }
    
        block[i] = b[0] ^ a[3] ^ a[2] ^ b[1] ^ a[1]; /* 2 * a0 + a3 + a2 + 3 * a1 */
        block[4+i] = b[1] ^ a[0] ^ a[3] ^ b[2] ^ a[2]; /* 2 * a1 + a0 + a3 + 3 * a2 */
        block[8+i] = b[2] ^ a[1] ^ a[0] ^ b[3] ^ a[3]; /* 2 * a2 + a1 + a0 + 3 * a3 */
        block[12+i] = b[3] ^ a[2] ^ a[1] ^ b[0] ^ a[0]; /* 2 * a3 + a2 + a1 + 3 * a0 */
        
    }
}

void InverseMixColumns(unsigned char *block){
    unsigned char a[16]; //Vetor original
    char i, j, k;
    for(i = 0; i < 16; i++)
        a[i] = block[i];
    
    for(i = 0; i < 4; i++){
        block[i] = (2 * a[i]) ^ (3 * a[i+4]) ^ a[i+8] ^ a[i+12];
        block[i+4] = a[i] ^ (2*a[i+4]) ^ (3*a[i+8]) ^ a[i+12];
        block[i+8] = a[i] ^ a[i+4] ^ (2*a[i+8]) ^ (3*a[i+12]);
        block[i+12] = (3*a[i]) ^ a[i+4] ^ a[i+8] ^ (2*a[i+12]); 
    }
    
}

//Etapa de adicionar a chave na rodada ao bloco. Basicamente um XOR bit a bit.
//É passado como argumento o ponteiro que aponta para o começo da string que representa o bloco de 16 bytes e a chave da rodada
void AddRoundKey(unsigned char *block, unsigned char *roundKey){
    
    unsigned char i;
    
    //Realiza uma operação XOR bit a bit entre bloco e chave de rodada
    for(i = 0; i<16; i++)
        block[i] ^= roundKey[i];
    
}

//Etapa inversa de AddRoundKey
//Uma vez que a AddRoundKey é um XOR bit a bit, seu inverso é exatamente a mesma coisa
//Essa função foi feita unicamente para manter padrão de ter funções inversas
void InverseAddRoundKey(unsigned char *block, unsigned char *roundKey){
    AddRoundKey(block, roundKey);
}

