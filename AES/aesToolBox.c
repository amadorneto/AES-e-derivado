//Recebe mensagem de 16 bytes, a chave de 16 bytes e um vetor vazio de 16 bytes para resultado
void Encrypt(unsigned char *message, unsigned char *key, unsigned char *result){
    unsigned char *roundKeys;
    int i;
    
    printf("1");

    //Copia estado inicial
    for(i = 0; i < 16; i++)
        result[i] = message[i];
    
    printf("2\n");
    
    //Gera chave de rodadas
    //roundKeys = KeyExpansion(key, roundKeys);
    roundKeys = KeyExpansion(key);
    
    printf("\n3\n");
    
    //Começa o processo de encriptação    
    AddRoundKey(result, roundKeys);
    
    printf("4\n");
    
    for(i = 1; i < 10; i++) {
        SubBytes(result);
        ShiftRows(result);
        MixColumns(result);
        AddRoundKey(result,&roundKeys[16*i]);
    }
    
    printf("5");
    
    //Ultima rodada
    SubBytes(result);
    ShiftRows(result);
    AddRoundKey(result,&roundKeys[160]);
    
    free(roundKeys);
    
}

//Recebe criptograma de 16 bytes, a chave de 16 bytes e um vetor vazio de 16 bytes para resultado
void Decrypt(unsigned char *crypto, unsigned char *key, unsigned char *result){
    
    unsigned char *roundKeys;
    int i;

    //Copia estado inicial
    for(i = 0; i < 16; i++)
        result[i] = crypto[i];
    
    roundKeys = malloc(176*sizeof(unsigned char));
    
    //Gera chave de rodadas
    //KeyExpansion(key, roundKeys);
    roundKeys = KeyExpansion(key);
    
    //Começa o processo de encriptação    
    InverseAddRoundKey(result, &roundKeys[160]);
    
    for(i = 9; i > 0; i--) {
        InverseShiftRows(result);
        InverseSubBytes(result);
        InverseAddRoundKey(result,&roundKeys[16*i]);
        InverseMixColumns(result);
        
    }
    
    //Ultima rodada
    InverseShiftRows(result);
    InverseSubBytes(result);
    InverseAddRoundKey(result, roundKeys);
    
}